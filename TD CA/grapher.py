import numpy as np
import matplotlib.pyplot as plt
from CA import TwoDimensionalCA

def graph_initial_to_final(initial_state, birth_rule, survival_rule, gen = 10):
    # Create a cellular automaton with the specified rule
    print(initial_state.shape)
    
    ca = TwoDimensionalCA(width=initial_state.shape[1], height=initial_state.shape[0],
                          birth_rule=birth_rule, survival_rule=survival_rule)

    # Set the initial state of the cellular automaton
    ca.grid = initial_state

    for i in range(gen):
        ca.evolve()

    # Get the evolved final state from the cellular automaton
    evolved_final_state = ca.grid

    # Plot the initial state
    plt.subplot(1, 3, 1)
    plt.imshow(initial_state, cmap='binary')
    plt.title('Initial State')
    plt.axis('off')

    # Plot the evolved final state
    plt.subplot(1, 3, 2)
    plt.imshow(evolved_final_state, cmap='binary')
    plt.title('Evolved Final State')
    plt.axis('off')


    # Show the plot
    plt.show()
    
